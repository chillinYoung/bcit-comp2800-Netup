function changeColor() {
  document.documentElement.style.setProperty('--color-point-mint', '#333333');
  document.documentElement.style.setProperty('--color-point-orange', '#66CCFF');
  document.documentElement.style.setProperty('--color-point-yellow', '#6666FF');
  document.documentElement.style.setProperty('--color-point-yellow-alpha', '#666666');
  document.documentElement.style.setProperty('--color-point-silver', '#666666');
  document.documentElement.style.setProperty('--color-bg', '#808080');
  document.documentElement.style.setProperty('--color-dark-text', '#E6E6E6');
  document.documentElement.style.setProperty('--color-dim-text', '#333333');
}

